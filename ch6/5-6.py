# 设置变量 age 的值，再编写一个 if-elif-else 结构，根据 age的值判断处于人生的哪个阶段
age = 50
if age < 2:
    print("You are a baby")
elif 4 > age >= 2:
    print("You are still toddle")
else:
    print("")
if 13 > age >= 4:
    print("You are a child")
elif 13 < age < 20:
    print("You are a teenager")
else:
    print("")
if 65 > age >= 20:
    print("You are a adult")
elif age >= 65:
    print("You are a old man")
else:
    print("")

